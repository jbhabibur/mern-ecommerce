import User from "../models/User.js";
import bcrypt from "bcryptjs";
import { asyncHandler } from "../middleware/error.middleware.js";

// 1. Get User Profile
// Fetches user data excluding the password field
export const getProfile = async (req, res) => {
  try {
    // req.user.id is populated by your verifyToken middleware
    const user = await User.findById(req.user.id).select("-password");

    if (!user) {
      return res.status(404).json({ message: "User not found" });
    }

    res.status(200).json({ user });
  } catch (error) {
    res.status(500).json({ message: "Server Error", error: error.message });
  }
};

// 2. Create User / Register
// Handles new user registration with email uniqueness check and password hashing
export const registerUser = async (req, res) => {
  try {
    const { firstName, lastName, email, password } = req.body;

    // Check if email is already registered
    const existingUser = await User.findOne({ email });
    if (existingUser)
      return res.status(400).json({ message: "Email already exists" });

    // Hash the password before saving to the database
    const salt = await bcrypt.genSalt(10);
    const hashedPassword = await bcrypt.hash(password, salt);

    const newUser = await User.create({
      firstName,
      lastName,
      email,
      password: hashedPassword,
    });

    res.status(201).json({
      message: "User registered successfully",
      userId: newUser._id,
    });
  } catch (error) {
    res
      .status(500)
      .json({ message: "Registration failed", error: error.message });
  }
};

// 3. Update Profile
// Updates non-sensitive user information
export const updateProfile = async (req, res) => {
  try {
    const { firstName, lastName, phone, birthday, gender, isSubscribed } =
      req.body;

    const updatedUser = await User.findByIdAndUpdate(
      req.user.id,
      {
        $set: {
          firstName,
          lastName,
          phone,
          birthday,
          gender,
          isSubscribed,
        },
      },
      { new: true, runValidators: true }, // 'new' returns the updated doc, 'runValidators' ensures Joi/Schema rules apply
    ).select("-password");

    res.status(200).json({
      message: "Profile updated successfully",
      user: updatedUser,
    });
  } catch (error) {
    res.status(500).json({ message: "Update failed", error: error.message });
  }
};

// 4. Change Password
// Verifies current password before allowing a password reset
export const changePassword = async (req, res) => {
  try {
    const { currentPassword, newPassword } = req.body;

    // We need the password field specifically here, so we use select("+password")
    const user = await User.findById(req.user.id).select("+password");
    if (!user) return res.status(404).json({ message: "User not found" });

    // Compare provided currentPassword with stored hash
    const isMatch = await bcrypt.compare(currentPassword, user.password);
    if (!isMatch)
      return res.status(400).json({ message: "Current password is incorrect" });

    // Hash the new password
    const salt = await bcrypt.genSalt(10);
    user.password = await bcrypt.hash(newPassword, salt);

    await user.save();
    res.status(200).json({ message: "Password updated successfully" });
  } catch (error) {
    res
      .status(500)
      .json({ message: "Password update failed", error: error.message });
  }
};

// 5. Update Profile Image
// Handles profile picture updates by saving the Cloudinary URL to the database
export const updateProfileImage = async (req, res) => {
  try {
    // Check if the file was successfully uploaded via the Multer-Cloudinary middleware
    if (!req.file) {
      return res
        .status(400)
        .json({ message: "Upload failed: No file provided" });
    }

    // req.file.path contains the secure URL generated by Cloudinary
    const imageUrl = req.file.path;

    // Update the user's photo field in the database and return the updated document
    const user = await User.findByIdAndUpdate(
      req.user.id,
      { photo: imageUrl },
      { new: true },
    ).select("-password");

    if (!user) {
      return res.status(404).json({ message: "User not found" });
    }

    res.status(200).json({
      success: true,
      message: "Profile image updated successfully!",
      photo: user.photo,
      user,
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: "Server error during image update",
      error: error.message,
    });
  }
};

// 6. Get all users except customers
